function export_regional_bar_data(filename,outdata,scenarios)

fid = fopen(filename,'wt')

fprintf(fid,'Scenario,Sexual (2014),Sexual (2015),Asexual (2014),Asexual (2015),Seed (2014),Seed (2015),Flower (2014), Flower (2015),Adult (2014),Adult (2015)\n');

for i = 1:length(scenarios)
    
    if i == 1
        int = 1;
    else
        int = (i*2)-1;
    end
    
    fprintf(fid,'%s,%10.4f,%10.4f,%10.4f,%10.4f,%10.4f,%10.4f,%10.4f,%10.4f,%10.4f,%10.4f\n',...
        scenarios{i},...
        outdata.Sexual(int,1),...
        outdata.Sexual(int+1,1),...
        outdata.Sexual(int,2),...
        outdata.Sexual(int+1,2),...
        outdata.Adult(int,1),...
        outdata.Adult(int+1,1),...
        outdata.Adult(int,2),...
        outdata.Adult(int+1,2),...
        outdata.Adult(int,3),...
        outdata.Adult(int+1,3));
end
fclose(fid)